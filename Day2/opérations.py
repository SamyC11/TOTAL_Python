x = 10
y = 2
z = 4

print(f"{x} plus {y} equals {x + y}")
print(f"{x} minus {y} equals {x - y}")
print(f"{x} times {y} equals {x * y}")
print(f"{x} divided {y} equals {x / y}")

print(f"{x} divided {z} equals {x / z}")
print(f"{x} modulo of {z} equals {x%z}")
print(f"{x} to the power of {z} equals {x**z}")
print(f"{x} to the power of 10 equals {x**10}")
print(f"the square root of {x} equals {x**0.5}")